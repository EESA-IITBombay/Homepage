<span class="subhead">APPENDIX A</span>
<p><br><font color="white">President's Nominee:</font> He will be the contact person between the faculty and the student.</p><br>
<p><font color="white">OFFICIAL POSTS</font><br>
</p><ul>
<li>Department General Secretary</li>
<li>Electrical Engineering Students' Association (EESA) General Secretary</li>
<li>2- Joint Secretaries</li>
<li>Post graduate representative</li>
<li>Web secretary</li>
<li>Alumni secretary</li>
<li>2nd year class representative</li>
<li>3rd year class representative</li>
<li>4th year class representative</li>
</ul>
<p></p>
<br>
<p><font color="white">ELIGIBILITY TO CONTEST</font><br>
</p><ul>
<li>All contestants should have academic commitments for at least 1 or more full semesters in the department.</li>
<li>Only those who have completed one full semester in the department can contest for the posts of Department General Secretary and EESA General Secretary.</li>
</ul>
<p></p>
<br>
<p><font color="white">FUNCTIONS AND RESPONSIBILITIES</font><br>
<font color="white">a) Department General Secretary</font><br>
</p><ul>
<li>will represent the student body at all meetings/events at the Department as well as the Institute level</li>
<li>will address general grievances of the students regarding all departmental facilities</li>
<li>should initiate efforts to make students identify themselves with the Department. like bringing out Department T-shirt etc.)</li>
<li>should arrange to get the department magazine by every academic year along with the publications secretary</li>
<li>will be responsible for seminars to be conducted in the
department and will co-ordinate with the faculty and the seminar secretary</li>
<li>will keep in regular touch with the class representatives and will coordinate all academic problems and feed back of students regarding courses</li>
<li> will be responsible for conducting awareness courses regarding computational lab and will coordinate with the computer secretary</li>
<li>should convene a general body meeting every semester</li>
<li>should arrange to nominate students from the Btech passing out batch for representing in training and placement activities</li>
<li>will nominate a suitable person, with approval of the Head of the Department to any post in the event of any elected office-bearer resigning from the post</li>
<li>will conduct the election of the next council at least 20 days before the last day of the spring semester</li>
</ul><p></p>
<p>
<font color="white">b) EESA General Secretary</font><br>
Each class shall elect a class representative who
</p><ul>
<li>will be responsible for organising all activities undertaken by EESA</li>
<li>will help to organize events that help foster greater interaction between faculty members and students.(example-sports, picnics, quiz etc.)</li>
<li>should collect EESA membership fees from first year UGs (membership fees Rs 100 or as decided by the Department Staff Students Committee(DSSC) from time to time). The membership record for the year should be maintained</li>
<li>should bring out a magazine on the occasion of department valedictory function</li>
<li>will be incharge of organising the Btech valedictory function and the department welcome for the first year Btech students</li>
<li>will co-ordinate with the Joint Secretary, EESA for the accomplishment of the above tasks</li>
</ul>
<p></p>
<p>
<font color="white">c) EESA Joint General Secretary</font><br>
Shall assist the EESA General Secretary in discharging his/her functions.
</p><ul>
<li>will be responsible for the publication of the monthly student magazine 'Background Hum'.</li>
<li>will be responsible for getting the department t-shirt printed, including arranging its sponsorship, and arriving at a suitable design. </li>
</ul>
<p>He will work in coordination with the department General Secretary in this.</p>
<p></p>
<p>
<font color="white">d) Post Graduate Representative</font><br>
The post-graduate students of the Department shall elect a person from amongst them to represent them on
</p><ul>
<li>all matters regarding their project/academic work</li>
<li>allotment of seminar/M.Tech. project</li>
<li>Training and placement related matters</li>
<li>will be in charge of organizing the post graduate valedictory function and department welcome for first year post graduate students</li>
<li>should collect EESA membership fees from first year PG's and Phd students ( duration membership fees Rs. 100/- from Mtech students and Rs. 100/- from Phd. scholars)</li>
<li>should maintain membership record of the year</li>
<li>any other issues that may be of concern to post-graduate students</li>
      </ul>
      <p><font color="white">e) Class Representative</font><br>Each class shall elect a class representative who</p>
      <ul>
        <li>will pass on information/messages from the faculty members to the class</li>
        <li>will convey the collective opinion of the class on issues of concern to faculty members</li>
        <li>should assist other office-bearers in carrying out their activities (valedictory functions, new entrants' welcome)</li>
      </ul>
      <br>
      <p><font color="white">ELECTION PROCEDURE SCHEDULE</font><br></p>
      <ul>
        <li>The election for all posts should be held on a single day, at least 20 days before the last day of the Spring Semester. However these cannot be held more than forty days before the last day of the spring semester.</li>
        <li>Head of the Department shall nominate any of the Faculty to act as the returning officer. The returning officer shall announce the election schedule- last date for filing nominations, last date for withdrawal, date and time of polling and declaration of results.</li>
        <li>There should be a gap of 2 days between the last date for withdrawal of nominations and actual polling date.</li>
        <li>Voting shall be by secret ballot. The candidate securing the largest number of votes polled shall be declared elected. In case of a tie, a fresh vote should be taken the next working day. In case no candidate files his nomination for any post, the Head of Department will nominate an eligible student for the post.</li>
      </ul>
      <br>
      <p><font color="white">ELIGIBILITY TO VOTE</font><br></p>
      <ul>
        <li>All students who have spent at least one full semester in the department can vote.</li>
        <li>First year undergraduate students can vote only for the post of their Class Representative.</li>
      </ul>
      <br>
      <p><font color="white">RULES FOR THE ELECTION</font><br></p>
      <ul>
        <li>Passed by a minimum of two-third majority in a General Body meeting, and each candidate has to submit a one page (A4) manifesto to the Returning Officer by the last day of withdrawal of candidature.</li>
        <li>The Returning Officer shall put up these manifesto's on the EESA or any other appropriate notice board till the date of election.</li>
        <li>Campaigning on the LAN is strictly prohibited unless prior permission of the system administrator is obtained.</li>
        <li>The decision of the Returning Officer on any issue related to the election shall be final and binding on all concerned.</li>
      </ul>
      <br>
