      <h2 class="font_2">Electrical Engineering Student Association (EESA)</h2>
      <p class="font_8">&nbsp;</p>

      <p>
        <font color="white">Name of the Association</font><br>
        The name of the Association shall be Electrical Engineering Students' Association, I.I.T. Bombay, hereinafter referred to as The Association.<br><br>
      </p>
      <p>
        <font color="white">Aims and Objectives</font><br>
      </p>
      <ul>
        <li>To further the knowledge and awareness in the field of Electrical and Electronics Engineering through meetings, discussions, quizzes and the like</li>
        <li>To create a spirit of brotherhood and co-operation amongst the members of the Association</li>
        <li>To develop leadership and initiative, and to inculcate a sense of responsibility amongst the members of the Association</li>
      </ul>
      <br>
      <p>
        <font color="white">Membership</font><br>
        The membership of the Association shall be open to the following:
      </p>
      <ul>
        <li>Students belonging to the Department of Electrical Engineering. (Students are all members having a Electrical Engineering roll number)</li>
        <li>Any other student with the approval of the executive</li>
        <li>The membership will be valid for a period of one year. The year begins from the month of July and ends on the last day of June of the following year.</li>
        <li>All members shall pay subscription at the rates as decided by the Executive Committee of the Association.</li>
        <li>The head of the department is the ex-officio President of the Association.</li>
      </ul>
      <br>
      <p>
        <font color="white">General Body</font><br>
        The general body will consist of all the members of the Association. All members are required to pay a yearly subscription fee as given in Appendix B.<br><br>
      </p>
      <p>
        <font color="white">Executive Committee</font><br>
        The Executive Committee, (hereinafter referred to as (the) Committee), shall be the Governing body of the Association and shall be composed of:<br>
      </p>
      <ul>
        <li><font color="white">President:</font>The head of the department is the ex-officio President of the Association.</li>
        <li><font color="white">Vice-President:</font>The Vice-President is nominated by the President and is a member of the faculty of the department. He/she will be nominated for a term of two years.</li>
        <li><font color="white">Treasurer:</font>The Treasurer is nominated by the President of the Association and is a staff member of the department. He/she will be nominated for a term of two years.</li>
        <li><font color="white">Department General Secretary:</font>A student elected by the student members themselves for a term of one year.</li>
        <li><font color="white">EESA General Secretary:</font>A student elected by the student members themselves for a term of one year.</li>
        <li><font color="white">Joint Secretaries:</font>Two students elected by the student members themselves for a term of one year.</li>
        <li><font color="white">Post-graduate's nominee:</font>A post-graduate student nominated by the President for a term of one year.</li>
        <li><font color="white">Web secretary:</font>A student nominated by the President for a term of one year.</li>
        <li><font color="white">Alumni Secretary:</font>A student nominated by the President for a term of one year.</li>
      </ul>
      <br>
      <p>
        <font color="white">Executive Committee</font><br>
        The Executive Committee holds the office for a term of one year (specified in the duration of membership). At the end of the term the President may request the existing committee to continue in office for a maximum of six weeks for the smooth transition of organizational responsibilities from the previous year's Executive Committee.<br><br>
      </p>
      <p><font color="white">Executive Committee Elections</font><br></p>
      <ul>
        <li>The elections to the Committee for the next year shall be held at the end of the current academic year. The elections will be held by a Election Officer as appointed by the President. It will be held by secret ballot on basis of simple majority.</li>
        <li>Co-option: The Committee will co-opt members in the event of resignation of any member from the Committee before his/her term is over or if an elected post is uncontested.</li>
      </ul>
      <br>
      <p><font color="white">Meetings of the Committee</font><br></p>
      <ul>
        <li>The general secretary shall convene the meetings and give normally two days notice of every meeting. The notice will also mention agenda.</li>
        <li>The President shall preside over all the meetings and in his absence a member of the Committee nominated by the President.</li>
        <li>The minutes of the meetings shall be recorded by the general secretary and confirmed at the next meeting.</li>
        <li>The absence of any member of the Committee shall not invalidate the proceedings of any meeting of the Committee.</li>
        <li>Quorum: Any three members shall constitute a quorum for the meeting.</li>
        <li>In all discussions an attempt will be made to reach a consensus, in the absence of which the resolutions shall be adopted on the basis of a simple majority of those present at the meeting.</li>
        <li>In case of a tie in the voting on any resolution in the meeting, the President shall resolve the tie.</li>
        <li>The President could call the Department General Secretary and the class representatives to the Meetings of the Committee. They would not have any voting rights in the Meetings.</li>
        <li>There shall be at least two meetings a semester.</li>
      </ul>
      <br>
      <p><font color="white">Power Of The Executive Committee</font><br></p>
      <ul>
        <li>To carry out the work of the association.</li>
        <li>To incur and defray all necessary expenses as provided in the budget to be adopted by the committee.</li>
        <li>To appoint sub-committees and to create any other office within the Association as and when required.</li>
      </ul>
      <br>
      <p><font color="white">General Body Meetings (GBM)</font><br></p>
      <ul>
<li>General Functions of the EESA will serve as General Body Meetings.</li>
<li>The GBM at the end of the year during which the Executive Committee will present the report of the achievements of the year. The GBM at the start of the year during which the Executive Committee will be introduced to the General Body.</li>
<li> The Executive Committee may at its discretion call as many GBM's as it deems fit.</li>
</ul>
<p></p>
<br>
<p><font color="white">Funds</font><br>
</p><ul>
<li>All subscriptions, donations, grants and other money of the Association shall be deposited in a registered bank in the name of the Association. The account of the Association shall be operated by any two of the following :
<ul><li style="list-style:decimal;">President</li><li style="list-style:decimal;">Treasurer</li><li style="list-style:decimal;">General Secretary</li></ul>
The names of these office bearers with the specimen signatures will be forwarded to the bank, every year, by the President, for the purpose of operating the account.</li>
<li>The surplus funds if any of the Association, whether of a revenue of capital nature shall not be distributed to the members but shall be utilized wholly towards the futherance of the objectives of the Association.</li>
<li>The Association will function strictly within its own resources.</li>
<li>The Treasurer shall place the Income and Expenditure Account duly audited by the person appointed by the Committee at the end of each academic year before the General Body for adoption.</li>
</ul>
<p></p>
<br>
<p><font color="white">Amendments</font><br>
Any amendment of the constitution will be effective only when -
</p><ul>
<li>Passed by a minimum of two-third majority in a General Body meeting, and</li>
<li>Approved by the President.</li>
</ul>
<p></p>
