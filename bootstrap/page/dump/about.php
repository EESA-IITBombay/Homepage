      <h2 class="font_2">Electrical Engineering Student Association (EESA)</h2>
      <p class="font_8">&nbsp;</p>
      <p class="font_7">Electrical Engineering Student Association (EESA) is a student body representing students within the Department of Electrical Engineering, IIT Bombay. Our goal is to organize social and professional activities which strengthen and enrich the community within the department, and to help publicize various research activities within the department. We organize social events, host talks by guest speakers and students, and help communicate with the department and the students</p>
      <p class="font_7">&nbsp;</p>
      <p class="font_7">Aims and Objectives:</p>
      <ul class="font_7">
        <li>
          <p class="font_7">To further the knowledge and awareness in the field of electrical and electronics engineering through meetings, discussions, quizzes and the like.</p>
        </li>
        <li>
          <p class="font_7">To create a spirit of brotherhood and co-operation among the members of the association.</p>
        </li>
        <li>
          <p class="font_7">To develop leadership and initiative, and to inculcate a sense of responsibility among the members of the association.</p>
        </li>
      </ul>
